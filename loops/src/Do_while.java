public class Do_while {
    public static void main(String[] args){
        int count=0;
        do{
            System.out.println("Hello there!!!");
            count++; //increment

        }while(count<10);

        int count1 = 10;

        do{
            System.out.println("number:"+count1);
            count1--;

        }while(count1>0);
    }
}
