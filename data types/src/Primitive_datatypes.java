
public class Primitive_datatypes {
	
	public static void main(String[] args) {
		
		
		// these are primitive datatypes
		
		
		//integer datatype
		
		byte a = -127; //ranges from -128 to 127
		short b = 32767; //ranges from -32768 to 32767
		int c = 2147483647; //ranges from -2147483648 to 2147483647
		//long d = 9223372036854775806; //ranges from -9223372036854775808 to 9223372036854775807
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		//System.out.println(d);
		
		
		//float datatype
		
		float e = 1.12365878f;
		System.out.println(e);
		
		double f = 1.1236558786645;
		System.out.println(f);
		
		
		//character datatype
		
		char g ='h';
		System.out.println(g);
		
		//Boolean data type
		
		boolean h = true;
		boolean i = false;
		System.out.println(h);
		System.out.println(i);
		
		
		
	}

}
